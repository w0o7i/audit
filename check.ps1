    if ([string]::IsNullOrWhiteSpace($path)) { continue }

    # 1. Защита по хэшам для файлов игры
    if ($path -match "bin\\x64|bin" -and $AllowedHashes.ContainsKey($mod.ModuleName)) {
        if ((Get-FileHash $path -Algorithm SHA256).Hash -ne $AllowedHashes[$mod.ModuleName]) {
            Write-Host "  ❌ КРИТИЧЕСКИ: ФАЙЛ $($mod.ModuleName) ИЗМЕНЕН!" -ForegroundColor Red
            $SCORE += 100
        }
        continue
