    $sig = Get-AuthenticodeSignature $path
    if ($sig.Status -ne "Valid") {
        Write-Host "  ❌ НЕПОДПИСАННАЯ DLL: $($mod.ModuleName)" -ForegroundColor Red
        $SCORE += 35
    }
}

